# ============ while 循环简单例子 ============

# 1. 最基础：条件为真就一直执行
i = 0           # 定义一个变量 i，初始值为 0
while i < 5:    # 当 i 小于 5 时，循环继续执行
    print(i)    # 打印当前 i 的值
    i = i + 1   # i 加 1，一定要改变条件，否则会死循环！
# 输出：0 1 2 3 4

print("---")    # 打印分隔线，方便区分不同例子

# 2. 累加：计算 1+2+3+...+10 的和
total = 0       # 用来存放累加结果，初始为 0
n = 1           # 从 1 开始累加
while n <= 10:  # 当 n 小于等于 10 时继续循环
    total = total + n  # 把 n 加到 total 里
    n = n + 1   # n 加 1，下次循环处理下一个数
print("1到10的和:", total)  # 打印结果 55

print("---")    # 打印分隔线

# 3. break - 提前跳出循环
count = 0       # 计数器，初始为 0
while True:     # True 表示条件永远为真，是死循环，必须用 break 才能退出
    count += 1  # count 加 1，等同于 count = count + 1
    if count == 3:   # 如果 count 等于 3
        break   # 立即跳出循环，后面的代码不再执行
    print(count)     # 打印 count 的值
# 输出：1 2

print("---")    # 打印分隔线

# 4. continue - 跳过本次，继续下一次
j = 0           # 定义一个变量 j，初始为 0
while j < 5:    # 当 j 小于 5 时继续循环
    j += 1      # j 加 1，放在 continue 前面，否则 j 永远不变会死循环
    if j == 3:  # 如果 j 等于 3
        continue    # 跳过本次循环剩余的代码，直接进入下一次循环
    print(j)    # 打印 j 的值（j 为 3 时不会执行到这里）
# 输出：1 2 4 5
