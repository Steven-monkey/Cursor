"""
工资条示例 - 基础版

目标：只练习 pandas 的最基础用法：
    1. 用字典创建 DataFrame
    2. 查看表格
    3. 做简单的“列运算”（新增列）

建议学习顺序：
    第 1 步：先只看 main() 里面的几行 print，理解大致流程。
    第 2 步：再看 build_salary_dataframe()，理解每一列的数据结构。
    第 3 步：打开 Python 交互环境（或者用你习惯的方式），
             把代码一小块一小块复制进去运行，看输出结果。
"""

import pandas as pd  # 导入 pandas 库，起别名为 pd（大家的通用写法）


def build_salary_dataframe() -> pd.DataFrame:
    """
    构造一个最简单的工资表 DataFrame。

    这里只关心三件事：
        1. 表里有哪些“列”（字段）
        2. 每一列的数据类型是什么样的（字符串 / 数字）
        3. 怎么基于已有列，算出一列新的数据
    """
    # 1. 用字典准备原始数据：键 = 列名，值 = 这一列的数据列表
    data = {
        "工号":['QSJT001', 'QSJT002', 'QSJT003', 'QSJT004', 'QSJT005', 'QSJT006', 'QSJT007'],
        # "姓名" 列：公司员工的名字（这里用的是《狂飙》中的人物）
        "姓名": ["安欣", "高启强", "李响", "孟钰", "高启盛", "小五", "程程"],
        # "部门" 列：每个员工所属的部门
        "部门": ["刑警队", "建工集团", "刑警队", "央视记者站", "建工集团", "建工集团", "刑警队"],
        # "基础工资" 列：每个人的基础工资（固定部分）
        "基础工资": [12000, 30000, 13000, 15000, 22000, 8000, 9000],
        # "绩效奖金" 列：每个人对应的绩效奖金（浮动部分）
        "绩效奖金": [5000, 8000, 4000, 7000, 6000, 2000, 2500],
        # "扣款" 列：各种扣除（比如社保、公积金、罚款等的合计，这里只是示例数据）
        "扣款": [800, 3000, 500, 600, 2000, 300, 400],
        # "是否一线民警" 列：使用布尔值 True/False 标记是否是一线民警
        "是否一线民警": [True, False, True, False, False, False, True],
    }

    # 2. 用 DataFrame 把字典“变成表格”
    df = pd.DataFrame(data)

    # 3. 新增一列：应发工资 = 基础工资 + 绩效奖金
    #    注意：这里不用写 for 循环，pandas 会自动按行做加法
    df["实发工资"] = df["基础工资"] + df["绩效奖金"]-df["扣款"]

    # 4. 返回这张表，后面可以继续拿来用
    return df


def main() -> None:
    """基础演示：构造工资表 + 打印出来看一看。"""
    # 调用我们自己写的函数，得到一张工资表（DataFrame）
    df_salary = build_salary_dataframe()

    print("===== 基础版：原始工资表 =====")
    print(df_salary.to_string(index=False))  # 直接打印 DataFrame，看看表格长什么样
    print("============= 结束 ==========")           # 打一个空行，让终端里的输出更清晰


if __name__ == "__main__":
    # 只有当“直接运行这个文件”时，才会执行 main()
    # 如果只是 import 进别的文件学习，这里不会自动运行
    main()
