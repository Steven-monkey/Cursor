"""
工资条示例 - 使用 pandas 做工资表处理，并用 matplotlib 简单可视化。

说明（简要版）：
    - 用字典构造 DataFrame（工资明细表）
    - 通过列运算新增“应发工资”“实发工资”
    - 按部门分组统计人数、平均实发工资、总实发工资
    - 用 matplotlib 画折线图、柱状图，并导出 Excel 结果
"""

# 导入数据分析和可视化需要的库
import pandas as pd
import matplotlib.pyplot as plt


def build_salary_dataframe() -> pd.DataFrame:
    """
    构造工资信息明细表。

    返回：
        包含所有员工工资数据的 DataFrame。
    """
    # 用字典准备原始数据：键是列名，值是该列所有行的数据列表
    data = {
        "姓名": ["安欣", "高启强", "李响", "孟钰", "高启盛", "小五", "程程"],          # 员工姓名
        "部门": ["刑警队", "建工集团", "刑警队", "央视记者站", "建工集团", "建工集团", "刑警队"],  # 所属部门
        # 固定工资
        "基础工资": [12000, 30000, 13000, 15000, 22000, 8000, 9000],
        "绩效奖金": [5000, 8000, 4000, 7000, 6000, 2000, 2500],                  # 奖金
        # 各类扣款
        "扣款": [800, 3000, 500, 600, 2000, 300, 400],
        # 是否一线民警
        "是否一线民警": [True, False, True, False, False, False, True],
    }

    # 字典 → DataFrame，相当于在内存中创建一张工资明细表
    df = pd.DataFrame(data)

    # 新增列“应发工资”（基础工资 + 绩效奖金）
    # pandas 支持“列与列”直接做运算，会对每一行自动计算
    df["应发工资"] = df["基础工资"] + df["绩效奖金"]

    # 新增列“实发工资”（应发工资 - 扣款），表示员工实际到手的工资
    df["实发工资"] = df["应发工资"] - df["扣款"]

    return df  # 后续统计、筛选、画图都基于这张表


# ------------------------------------------------------------------------------
# 函数2：按部门分组统计（groupby + agg）
# ------------------------------------------------------------------------------

def analyze_by_department(df: pd.DataFrame) -> pd.DataFrame:
    """
    这个函数采用 Python 的“函数定义语法”来构造，意思如下：

    - def 是定义函数的关键字。"analyze_by_department" 是函数名，意为“按部门分析”。
    - 括号内 "df: pd.DataFrame" 表示参数 df，类型标注为 pandas 的 DataFrame，也就是工资明细表。
    - 箭头 "-> pd.DataFrame" 是返回类型标注，表示此函数最终会返回一个 DataFrame 结果。
    - 冒号 : 表示函数体开始。
    - 函数体内部一般会进行部门分组、统计等处理，最终返回结果表。

    这种构造方式叫“带类型注解的函数定义”，能够让代码更易读、易维护，也方便编辑器或工具智能提示和类型检查。
    """
    pass  # 这里只做解释，实际实现见下文
    """
    按部门汇总统计人数、平均实发工资和实发工资总额。

    参数：
        df: 工资明细表。

    返回：
        以“部门”为分组键的统计结果表。
    """
    # groupby + agg：按部门分组后对每组做多项统计，再按总额降序排
    grouped = (
        # 按“部门”分组；as_index=False 表示部门仍是普通列
        df.groupby("部门", as_index=False)
        .agg(                                   # agg 指定每个分组要计算的指标
            人数=("姓名", "count"),             # 每个部门有多少行（多少人）
            平均实发工资=("实发工资", "mean"),   # 部门平均实发工资
            实发工资总额=("实发工资", "sum"),     # 部门实发工资总额
        )
        .sort_values("实发工资总额", ascending=False)  # 再按“实发工资总额”这一列做降序排序
    )

    return grouped  # 返回部门统计结果


# ------------------------------------------------------------------------------
# 函数3：筛选高收入员工（布尔索引 + 排序）
# ------------------------------------------------------------------------------

def filter_high_salary(df: pd.DataFrame, threshold: int = 20000) -> pd.DataFrame:
    """
    筛选实发工资不低于给定阈值的员工，并按实发工资从高到低排序。

    参数：
        df: 工资明细表。
        threshold: 实发工资下限（默认 20000）。

    返回：
        满足条件的新 DataFrame。
    """
    # 布尔索引：df[条件] 只保留条件为 True 的行，再按实发工资降序排列
    result = df[df["实发工资"] >= threshold].sort_values("实发工资", ascending=False)

    return result


# ------------------------------------------------------------------------------
# 函数4：可视化工资数据（matplotlib.pyplot）
# ------------------------------------------------------------------------------

def visualize_salary(df: pd.DataFrame, dept_summary: pd.DataFrame) -> None:
    """
    用折线图和柱状图对工资数据做简单可视化。

    图1：各员工实发工资折线图；
    图2：各部门平均实发工资柱状图。
    """
    # 设置支持中文和正常显示负号的字体配置
    plt.rcParams["font.sans-serif"] = ["SimHei", "Microsoft YaHei", "SimSun"]
    plt.rcParams["axes.unicode_minus"] = False

    # =========================
    # 图1：各员工实发工资折线图
    # =========================
    plt.figure(figsize=(8, 5))  # 新建一个画布

    # 折线图：横轴为姓名，纵轴为实发工资
    plt.plot(df["姓名"], df["实发工资"], marker="o", color="steelblue")

    plt.title("各员工实发工资")
    plt.xlabel("姓名")
    plt.ylabel("实发工资（元）")
    plt.xticks(rotation=45)
    plt.tight_layout()
    plt.savefig("工资条示例/折线图.png")  # 保存图片
    plt.show()

    # ==============================
    # 图2：各部门平均实发工资柱状图
    # ==============================
    plt.figure(figsize=(8, 5))  # 新建第二个画布

    # 柱状图：横轴为部门，纵轴为平均实发工资
    plt.bar(
        dept_summary["部门"],            # 柱子的 x 位置（部门）
        dept_summary["平均实发工资"],    # 柱子的高度（平均实发工资）
        color="coral",                  # 柱子的填充颜色
        edgecolor="black",              # 柱子的边框颜色
    )

    plt.title("各部门平均实发工资")
    plt.xlabel("部门")
    plt.ylabel("平均实发工资（元）")
    plt.xticks(rotation=30)

    plt.tight_layout()
    plt.savefig("工资条示例/柱状图.png")
    plt.show()


# ------------------------------------------------------------------------------
# 主函数：从这里开始按顺序执行各个步骤
# ------------------------------------------------------------------------------

def main() -> None:
    """
    主程序入口：依次完成“构建工资表 → 部门统计 → 可视化 → 导出 Excel”。
    """
    # 构建工资明细表
    df_salary = build_salary_dataframe()

    # 部门汇总统计（人数、平均实发工资、实发工资总额）
    dept_summary = analyze_by_department(df_salary)

    # 简单可视化（折线图 + 柱状图，并保存为图片）
    visualize_salary(df_salary, dept_summary)

    # 导出工资明细表：index=False 表示不把行索引写入 Excel
    df_salary.to_excel("工资条示例/工资明细_狂飙人物.xlsx", index=False)

    dept_summary.to_excel("工资条示例/部门工资统计_狂飙人物.xlsx", index=False)


# ------------------------------------------------------------------------------
# 程序入口：只有“直接运行”这个文件时，才会执行 main()
# ------------------------------------------------------------------------------

if __name__ == "__main__":
    main()
