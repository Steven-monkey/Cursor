with open("我爱你.txt", "a+", encoding="utf-8") as file:
    str = "你为什么不说话？"
    file.write(str)
    new_context = file.read()
    print(new_context)

# 模式	说明
# "r"	只读（默认）。文件必须存在，否则报错
# "w"	只写。文件存在会清空，不存在会新建
# "a"	追加写。在文件末尾追加，文件不存在会新建
# "x"	独占创建。文件必须不存在，否则报错；用于新建文件

# 组合模式（读写）
# 模式	说明
# "r+"	读写。文件必须存在，指针在开头
# "w+"	读写。会清空或新建文件，指针在开头
# "a+"	读写。追加模式，文件不存在会新建，指针在末尾
# "x+"	读写。独占创建并读写，文件必须不存在
# 二进制模式（加 b）
# 模式	说明
# "rb"	二进制只读
# "wb"	二进制只写（会清空）
# "ab"	二进制追加
# "rb+" / "r+b"	二进制读写
# "wb+" / "w+b"	二进制读写（清空或新建）
# "ab+" / "a+b"	二进制追加并可读
