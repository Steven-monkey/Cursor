"""
演示 Python 中常见的几种“找文件路径”的方式，以及各自的适用场景和注意点。

1. 直接写相对路径：简单，但依赖“当前工作目录”，换目录运行易出错
2. os.path.join：按段拼接，自动适配不同系统的路径分隔符
3. 基于 __file__：以“当前脚本所在目录”为基准，不受运行目录影响，适合打包、部署
4. pathlib.Path：Python 3.4+ 推荐方式，写法清晰，支持链式操作

假设项目结构：
    2.code/
    ├─ 23.文件路径.py      （本脚本）
    └─ 部门排班系统/
         └─ demo.txt
"""

import os
from pathlib import Path

# =============================================================================
# 方式一：直接写相对路径（简单，但不灵活）
# =============================================================================
# 以“当前工作目录”（cwd）为起点。
# 当前工作目录 = 你在终端执行 python 时所在的目录，而不是脚本所在目录。
# 例如在 e:\Cursor\ 下执行 python 2.code/23.文件路径.py，则 cwd 为 e:\Cursor\
# 此时 "部门排班系统/demo.txt" 会被解析为 e:\Cursor\部门排班系统\demo.txt
# 若在 e:\Cursor\2.code\ 下执行，则解析为 e:\Cursor\2.code\部门排班系统\demo.txt
# 因此：换目录运行脚本时，相对路径可能指向错误位置。
relative_path = "部门排班系统/demo.txt"
print("方式一，相对路径：", relative_path)

# =============================================================================
# 方式二：os.path.join 按段拼接路径
# =============================================================================
# 用法：os.path.join(片段1, 片段2, ...)
# 作用：自动使用当前系统正确的路径分隔符：
#   - Windows 用反斜杠 \  (例如：部门排班系统\demo.txt)
#   - Linux / Mac 用正斜杠 / (例如：部门排班系统/demo.txt)
# 适用：需要跨平台时，避免手写 \ 或 / 导致兼容问题。
# 注意：仍然是“相对于当前工作目录”的路径，和方式一一样受 cwd 影响。
join_path = os.path.join("部门排班系统", "demo.txt")
print("方式二，os.path.join：", join_path)

# =============================================================================
# 方式三：基于当前脚本文件所在目录构造路径（推荐用于“脚本同目录资源”）
# =============================================================================
# __file__：内置变量，表示当前 .py 脚本文件自身的路径（字符串）。
# 例如：e:\Cursor\2.code\23.文件路径.py
#
# os.path.dirname(路径)：返回路径中的“目录部分”，去掉文件名。
# 例如：os.path.dirname("e:/a/b/c.py") → "e:/a/b"
#
# 组合使用：先得到脚本所在目录，再拼出目标文件的绝对路径。
# 这样无论从哪个目录运行脚本，都能正确找到“脚本同级”下的资源文件。
current_file = __file__                      # 当前脚本完整路径
current_dir = os.path.dirname(current_file)  # 当前脚本所在目录
abs_path = os.path.join(current_dir, "部门排班系统", "demo.txt")
print("方式三，基于 __file__ 的绝对路径：", abs_path)

# =============================================================================
# 方式四：pathlib.Path 构建路径（Python 3.4+ 推荐）
# =============================================================================
# Path 是面向对象的路径类，支持用 / 运算符拼接，写法更直观。
# 相对路径示例：
path_obj = Path("部门排班系统") / "demo.txt"
print("方式四，pathlib.Path 相对路径：", path_obj)

# 绝对路径：Path(__file__) 得到当前脚本的 Path 对象，.parent 表示其父目录（脚本所在目录）
base_dir = Path(__file__).parent
abs_path_pathlib = base_dir / "部门排班系统" / "demo.txt"
print("方式四，pathlib.Path 绝对路径：", abs_path_pathlib)