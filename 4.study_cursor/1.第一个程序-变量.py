# 1.第一个程序-变量.py
# 下面是第一个Python程序的带注释示例

# 定义一个字符串变量long_name，并赋予一段名言
long_name = "人民终将胜利，那里有压迫，那里就有反抗。"
# 定义一个年龄变量age，赋值为12
age = 12
# 定义一个金钱变量money，赋值为22222
money = 22222

# 打印long_name变量的内容
print(long_name)
# 打印age变量的内容
print(age)
# 打印money变量的内容
print(money)
# 将age加1，即年龄加一岁
age = age+1
# 打印加1后的age变量的内容
print(age)
