import pandas as pd

# ==============================
# 一、数据准备相关注释
# ==============================
data = {
    # 1. 字段说明/含义备注 ---------------------------
    "姓名": ["安欣", "高启强", "李响", "孟钰", "高启盛", "小五", "程程"],            # 员工姓名
    "部门": ["刑警队", "建工集团", "刑警队", "央视记者站", "建工集团", "建工集团", "刑警队"],   # 所属部门
    "基础工资": [12000, 30000, 13000, 15000, 22000, 8000, 9000],                  # 固定工资部分
    "绩效奖金": [5000, 8000, 4000, 7000, 6000, 2000, 2500],                      # 浮动奖金部分
    "扣款": [800, 3000, 500, 600, 2000, 300, 400],                               # 各项扣除（社保、公积金等）
    "是否一线民警": [True, False, True, False, False, False, True],                # 布尔值，是否是一线民警
}

# ==============================
# 二、DataFrame 构建和基础操作注释
# ==============================
# - 用 pandas DataFrame 方法，将字典 data 转为表格
# - DataFrame 是 pandas 用于表示二维表格（如 Excel/数据库表）的核心结构
# - 后续对整张表格的各种操作都以 df 变量为基础进行
df = pd.DataFrame(data)

# 计算新列“实发工资”：基础工资 + 绩效奖金 - 扣款
df["实发工资"] = df["基础工资"] + df["绩效奖金"] - df["扣款"]

# ==============================
# 三、导出文件相关注释（备忘/可选）
# ==============================
# to_excel 方法的 index 参数：默认 True，会额外导出一列行号
# 如果不想要行号，使用 index=False
# df.to_excel("工资条示例/工资表.xlsx", index=False)   # 导出时包含行号（默认）
# df.to_excel("工资表_no_index.xlsx", index=False)    # 导出时不包含行号，只导出你的数据列

# ==============================
# 四、数据筛选相关注释
# ==============================
# 数据筛选说明
# - 第一个 df：指定筛选的数据表（上一行创建的 DataFrame）
# - 第二个 df["实发工资"] < 20000 ：筛选条件（实发工资小于 20000）
# - 语句整体：从工资表 df 里，选出实发工资小于 20000 的所有行，组成一个新的 DataFrame，命名为 high_salary
high_salary = df[df["实发工资"] < 20000]

# # 读取已存在的工资表 Excel（如有需要，可用下面代码读取）
# # df=pd.read_excel("工资条示例/工资表.xlsx")

print(high_salary)
