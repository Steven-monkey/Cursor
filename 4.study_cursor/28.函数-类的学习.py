# =============================================================================
# 一、函数（Function）示例
# =============================================================================
# 函数是一段可以重复使用的代码块

# -----------------------------------------------------------------------------
# 1.  basic - 基础函数（无参数）
# -----------------------------------------------------------------------------

# 定义一个名为 say_hello 的函数
def say_hello():
    # 函数体：打印问候语
    print("你好，欢迎学习Python！")


# 调用函数，执行其中的代码
say_hello()


# -----------------------------------------------------------------------------
# 2. 带参数的函数
# -----------------------------------------------------------------------------

# 定义一个带参数 name 的函数
def greet(name):
    # 使用传入的参数拼接字符串
    message = "你好，" + name + "！"
    # 打印结果
    print(message)


# 调用时传入参数 "小明"
greet("小明")  # 输出：你好，小明！


# -----------------------------------------------------------------------------
# 3. 带返回值的函数
# -----------------------------------------------------------------------------

# 定义计算两数之和的函数
def add(a, b):
    # 计算 a + b
    result = a + b
    # 用 return 返回结果
    return result


# 调用函数并接收返回值
sum_result = add(3, 5)
# 打印结果
print(sum_result)  # 输出：8


# =============================================================================
# 二、类（Class）示例
# =============================================================================
# 类是一种"模板"，用来创建具有相同属性和方法的对象

# -----------------------------------------------------------------------------
# 1. 简单的类 - Dog 狗狗类
# -----------------------------------------------------------------------------

# 定义一个名为 Dog 的类（表示狗）
class Dog:
    # 构造函数 __init__：创建对象时自动执行
    def __init__(self, name, age):
        # self 代表对象本身，把 name 存到对象里
        self.name = name
        # 把 age 存到对象里
        self.age = age

    # 类的方法：狗狗叫
    def bark(self):
        # 使用 self.name 访问对象的属性
        print(self.name + " 在叫：汪汪汪！")


# 创建 Dog 对象，传入名字和年龄
my_dog = Dog("小黑", 3)
# 调用 bark 方法
my_dog.bark()  # 输出：小黑 在叫：汪汪汪！


# -----------------------------------------------------------------------------
# 2. 稍完整的类 - Student 学生类
# -----------------------------------------------------------------------------

# 定义一个表示学生的类
class Student:
    # 创建学生对象时的初始化方法
    def __init__(self, name, score):
        # 学生姓名
        self.name = name
        # 学生成绩
        self.score = score

    # 方法：打印学生信息
    def show_info(self):
        # 格式化字符串，打印姓名和成绩
        print(f"学生：{self.name}，成绩：{self.score}分")

    # 方法：判断是否及格
    def is_pass(self):
        # 60分及以上为及格
        if self.score >= 60:
            return True
        else:
            return False


# 创建学生对象
student1 = Student("小明", 85)
student2 = Student("小红", 55)

# 调用方法
student1.show_info()  # 输出：学生：小明，成绩：85分
print(student1.is_pass())  # 输出：True

student2.show_info()  # 输出：学生：小红，成绩：55分
print(student2.is_pass())  # 输出：False


# =============================================================================
# 三、封装（Encapsulation）一步一步学
# =============================================================================
# 封装 = 把「数据」和「操作数据的方法」打包到一起，方便管理和调用

# -----------------------------------------------------------------------------
# 第一步：想清楚要封装什么
# -----------------------------------------------------------------------------
# 比如：手机有品牌、电量，可以打电话、充电
# 数据：brand(品牌)、battery(电量)
# 方法：call(打电话)、charge(充电)


# -----------------------------------------------------------------------------
# 第二步：用 class 定义类，用 __init__ 存数据（属性）
# -----------------------------------------------------------------------------

# 定义一个手机类
class Phone:
    # __init__ 里封装数据：品牌、电量
    def __init__(self, brand, battery):
        # self.brand 就是"属性"，存品牌
        self.brand = brand
        # self.battery 存电量
        self.battery = battery

    # 第三步：写方法（操作数据的行为）
    # 方法1：打印信息
    def show_info(self):
        print(f"手机品牌：{self.brand}，剩余电量：{self.battery}%")

    # 方法2：充电（让电量增加）
    def charge(self, amount):
        self.battery = self.battery + amount
        print(f"充电中... 当前电量：{self.battery}%")


# -----------------------------------------------------------------------------
# 第四步：创建对象并调用
# -----------------------------------------------------------------------------

# 用类创建对象 = 实例化
my_phone = Phone("华为", 50)

# 调用方法：对象.方法名()
my_phone.show_info()   # 输出：手机品牌：华为，剩余电量：50%
my_phone.charge(20)    # 输出：充电中... 当前电量：70%
my_phone.show_info()   # 输出：手机品牌：华为，剩余电量：70%


# -----------------------------------------------------------------------------
# 再举一个例子：计算器
# -----------------------------------------------------------------------------

class Calculator:
    # 封装：只存一个"上一次的结果"
    def __init__(self):
        self.result = 0

    def add(self, a, b):
        self.result = a + b
        return self.result

    def show_result(self):
        print(f"计算结果：{self.result}")


# 创建计算器，调用方法
calc = Calculator()
calc.add(3, 5)    # 得到 8
calc.show_result()  # 输出：计算结果：8


# =============================================================================
# 四、概念速查表
# =============================================================================
# | 概念          | 说明                                         |
# |---------------|----------------------------------------------|
# | 封装          | 把数据(属性)和操作(方法)打包在类里           |
# | 函数 def      | 封装可重复使用的代码，可接收参数和返回值     |
# | 类 class      | 对象的"蓝图"或"模板"                         |
# | __init__      | 创建对象时自动执行，用来存属性               |
# | self          | 代表对象本身，用于在方法内访问属性           |
# | 属性          | 对象存储的数据（如 self.name）               |
# | 方法          | 类中定义的函数（第一个参数通常是 self）      |
# | 调用          | 对象.方法名() 或 对象.方法名(参数)           |
